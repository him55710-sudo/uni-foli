      setIsTyping(true);
      try {
        await requestGuidedSuggestions(text);
      } catch (error) {
        console.error('Guided suggestion failed:', error);
        toast.error('주제 제안 생성에 실패했습니다.');
      } finally {
        setIsTyping(false);
      }
      return;
