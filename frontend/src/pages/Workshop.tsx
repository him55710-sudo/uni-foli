    setIsTyping(true);
    setChatMeta(null);

    const foliId = crypto.randomUUID();
    setMessages(prev => [...prev, { id: foliId, role: 'foli', content: '' }]);

    let accumulated = '';
    let pendingDelta = '';
    let streamedPatch: WorkshopDraftPatchProposal | null = null;
    let flushTimer: number | null = null;

    const flushBufferedDelta = () => {
      if (!pendingDelta) {
        flushTimer = null;
        return;
      }
      accumulated += pendingDelta;
      pendingDelta = '';
      flushTimer = null;
      setMessages(prev => prev.map(message => (message.id === foliId ? { ...message, content: accumulated } : message)));
    };

    try {
      const raw = await streamFoliReply(
        isProjectBacked ? projectId : undefined,
        workshopState?.session.id,
        text,
        documentContent,
        workshopMode,
        structuredDraft,
        delta => {
          pendingDelta += delta;
          if (flushTimer === null) {
            flushTimer = window.setTimeout(flushBufferedDelta, 40);
          }
        },
        meta => {
          setChatMeta(meta);
          if (
            meta.coauthoring_mode === 'planning' ||
            meta.coauthoring_mode === 'outline' ||
            meta.coauthoring_mode === 'section_drafting' ||
            meta.coauthoring_mode === 'revision'
          ) {
            setWorkshopMode(meta.coauthoring_mode);
            setStructuredDraft((prev) => ({ ...prev, mode: meta.coauthoring_mode as WorkshopMode }));
          }
        },
        patch => {
          const normalizedPatch = normalizeStructuredDraftPatch(patch);
          if (normalizedPatch) {
            streamedPatch = normalizedPatch;
            setPendingDraftPatch(normalizedPatch);
          }
        },
      );

      if (flushTimer !== null) {
        window.clearTimeout(flushTimer);
        flushTimer = null;
      }
      flushBufferedDelta();

      const extracted = extractPatchTagFromRaw(raw);
      const resolvedPatch = streamedPatch || extracted.patch;
      const responseContent = extracted.cleaned || raw || accumulated || '응답을 생성하지 못했습니다.';
      const shouldAutoApplyPatch =
        resolvedPatch !== null &&
        coauthoringTier !== 'basic' &&
        (isPatchAcceptanceMessage(text) || isSectionDraftIntent(text));

      if (resolvedPatch && shouldAutoApplyPatch) {
        const autoApplied = applyPatchToDraft(resolvedPatch, true, false);
        if (!autoApplied) {
          setPendingDraftPatch(resolvedPatch);
        }
      }

      setMessages(prev =>
        prev.map(message =>
          message.id === foliId
            ? {
                ...message,
                content: responseContent,
                draftPatch: resolvedPatch && !shouldAutoApplyPatch ? resolvedPatch : undefined,
              }
            : message,
        ),
      );
    } catch (error) {
      console.error('AI reply stream failed:', error);
      setMessages(prev =>
        prev.map(message =>
          message.id === foliId
            ? {
                ...message,
                content: buildFoliFallback(text),
              }
            : message,
        ),
      );
    } finally {
      setIsTyping(false);
    }
