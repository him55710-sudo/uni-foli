function buildFoliFallback(message: string) {
  const clean = message.toLowerCase().trim();
  const greetings = ['안녕', '반가워', '하이', '안녕하세요', 'hello', 'hi', 'hey'];

  if (greetings.some(token => clean.includes(token))) {
    return [
      '안녕하세요. Uni Folia 워크숍입니다.',
      '',
      '현재 일부 고급 기능이 제한되어 있어도, 초안 구조화와 문장 개선은 계속 도와드릴 수 있습니다.',
    ].join('\n');
  }

  return [
    '현재 연결 문제로 자동 근거 검증이 제한되어 있습니다.',
    '',
    '대신 아래 작업은 바로 도와드릴 수 있어요:',
    '1. 연구 질문 구체화',
    '2. 필요한 근거 목록 정리',
    '3. 보고서 구조 설계',
  ].join('\n');
}

async function streamFoliReply(projectId: string | undefined, message: string, onDelta?: (delta: string) => void) {
  const baseUrl = resolveApiBaseUrl();
  const token = auth?.currentUser ? await auth.currentUser.getIdToken() : null;
  let response: Response;

  try {
    response = await fetch(`${baseUrl}/api/v1/drafts/chat/stream`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', ...(token ? { Authorization: `Bearer ${token}` } : {}) },
      body: JSON.stringify({ project_id: projectId, message, reference_materials: [] }),
    });
  } catch {
    if (!projectId) return buildFoliFallback(message);
    throw new Error('NETWORK_FAIL');
  }

  if (!response.ok || !response.body) {
    if (!projectId) return buildFoliFallback(message);
    if (response.status === 429) throw new Error('RATE_LIMIT');
    if (response.status === 401 || response.status === 403) throw new Error('AUTH_REQUIRED');
    throw new Error('STREAM_FAIL');
  }

  const reader = response.body.getReader();
  const decoder = new TextDecoder();
  let full = '';
  let buffer = '';

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;

    buffer += decoder.decode(value, { stream: true });
    const chunks = buffer.split('\n\n');
    buffer = chunks.pop() || '';

    for (const event of chunks) {
      const line = event.split('\n').find(item => item.startsWith('data:'));
      if (!line) continue;

      let payload: Record<string, unknown>;
      try {
        payload = JSON.parse(line.replace(/^data:\s*/, ''));
      } catch {
        continue;
      }

      if (payload.done) break;
      if (typeof payload.error === 'string' && payload.error.trim()) throw new Error(payload.error.trim());

      if (typeof payload.token === 'string' && payload.token) {
        full += payload.token;
        onDelta?.(payload.token);
      }
    }
  }

  return full.trim();
}

