function clipForFallback(text: string, length = 140) {
  const normalized = text.trim().replace(/\s+/g, ' ');
  if (normalized.length <= length) return normalized;
  return `${normalized.slice(0, length).trimEnd()}...`;
}

function buildDemoFallbackReply(message: string) {
  return [
    '### Demo fallback',
    '',
    'The backend chat connection is unavailable right now, so I switched to a local safe response.',
    'I cannot verify evidence or generate a grounded paragraph in this mode, but I can still help with structure and next-safe actions.',
    '',
    `Your prompt: "${clipForFallback(message)}"`,
    '',
    'Try one of these next-safe actions:',
    '1. Ask me to narrow the topic into one research question.',
    '2. Ask me to list the evidence you still need.',
    '3. Ask me to build a comparison table outline.',
  ].join('\n');
}

async function streamFoliReply(projectId: string | undefined, message: string, onDelta?: (d: string) => void) {
  const baseUrl = resolveApiBaseUrl();
  const token = auth?.currentUser ? await auth.currentUser.getIdToken() : null;
  let res: Response;

  try {
    res = await fetch(`${baseUrl}/api/v1/drafts/chat/stream`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', ...(token ? { Authorization: `Bearer ${token}` } : {}) },
      body: JSON.stringify({ project_id: projectId, message, reference_materials: [] }),
    });
  } catch (error) {
    if (!projectId) {
      return buildDemoFallbackReply(message);
    }
    throw new Error('NETWORK_FAIL');
  }

  if (!res.ok || !res.body) {
    if (!projectId) {
      return buildDemoFallbackReply(message);
    }
    if (res.status === 429) throw new Error('RATE_LIMIT');
    if (res.status === 401 || res.status === 403) throw new Error('AUTH_REQUIRED');
    throw new Error('STREAM_FAIL');
  }
  const reader = res.body.getReader();
  const decoder = new TextDecoder();
  let full = '';
  let buffer = '';

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    
    buffer += decoder.decode(value, { stream: true });
    const lines = buffer.split('\n\n');
    buffer = lines.pop() || '';

    for (const e of lines) {
      const line = e.split('\n').find(l => l.startsWith('data:'));
      if (line) {
        let p: Record<string, unknown>;
        try {
          p = JSON.parse(line.replace(/^data:\s*/, ''));
        } catch (err) {
          console.error('Payload parse error', err);
          continue;
        }

        if (typeof p.error === 'string' && p.error.trim()) {
          throw new Error(p.error.trim());
        }

        if (typeof p.token === 'string' && p.token) { 
          full += p.token; 
          onDelta?.(p.token); 
        }
      }
    }
  }
  return full.trim();
}

export function Workshop() {
  const { projectId } = useParams<{ projectId: string }>();
  const location = useLocation();
  const [workshopState, setWorkshopState] = useState<WorkshopStateResponse | null>(null);
  const [messages, setMessages] = useState<Message[]>([]);
  const [input, setInput] = useState('');
  const [isTyping, setIsTyping] = useState(false);
  const [isSessionLoading, setIsSessionLoading] = useState(true);
  const [documentContent, setDocumentContent] = useState('');
  const [qualityLevel, setQualityLevel] = useState<QualityLevel>('mid');
  const [advancedMode, setAdvancedMode] = useState(false);
  const [renderArtifact, setRenderArtifact] = useState<DraftArtifact | null>(null);
  const [diagnosisReport, setDiagnosisReport] = useState<any>(null);
  const [showDiagnosis, setShowDiagnosis] = useState(false);
  const [isRendering, setIsRendering] = useState(false);
  const [docHistory, setDocHistory] = useState<string[]>([]);
  const [historyIndex, setHistoryIndex] = useState(-1);
  const [pendingSuggestion, setPendingSuggestion] = useState<string | null>(null);
  const messagesEndRef = useRef<HTMLDivElement>(null);

  const questStart = useMemo(() => readQuestStart(), []);
  const initialMajor = useMemo(() => (location.state as any)?.major || '미설정', [location.state]);
  const isProjectBacked = !!projectId && projectId !== 'demo';
  const fileName = useMemo(() => `${(questStart?.title || '초안').replace(/\s+/g,'_')}.hwpx`, [questStart]);

  useEffect(() => {
    // Safety check for localStorage quality level
    const savedLevel = localStorage.getItem('folia_quality_level');
    if (savedLevel === 'low' || savedLevel === 'mid' || savedLevel === 'high') {
      setQualityLevel(savedLevel as QualityLevel);
    }

    setDocumentContent(questStart?.document_seed_markdown || `# [탐구 초안] ${questStart?.title || '주제'}\n\n**전공:** ${initialMajor}\n\n## 1. 탐구 동기`);
    
    if (isProjectBacked) {
       initWorkshop();
    } else {
       setIsSessionLoading(false);
       setMessages([{ id: 'init', role: 'foli', content: '체험 모드입니다. 자유롭게 대화해 보세요!' }]);
    }
  }, [projectId]);

  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  }, [messages]);

  useEffect(() => {
    if (!workshopState?.session.id || !documentContent) return;
    const t = setTimeout(() => {
      api.put(`/api/v1/workshops/${workshopState.session.id}/drafts/latest`, {
        document_content: documentContent
      }).catch(err => console.error("Autosave failed", err));
    }, 3000);
    return () => clearTimeout(t);
  }, [documentContent, workshopState?.session.id]);

  useEffect(() => {
    if (!documentContent.includes('### [AI')) return;
    const normalizedMarker = documentContent.replace(/### \[AI[^\]]*\]/g, AI_SUGGESTION_HEADER);
    const normalizedContent = normalizedMarker.includes(AI_SUGGESTION_NOTE)
      ? normalizedMarker
      : normalizedMarker.replace(AI_SUGGESTION_HEADER, `${AI_SUGGESTION_HEADER}\n${AI_SUGGESTION_NOTE}`);
    if (normalizedContent !== documentContent) {
      setDocumentContent(normalizedContent);
    }
  }, [documentContent]);

  const initWorkshop = async () => {
    setIsSessionLoading(true);
    try {
      const sessions = await api.get<WorkshopSessionResponse[]>(`/api/v1/workshops?project_id=${projectId}`);
      const active = sessions.find(s => s.status !== 'completed');
      const state = active 
        ? await api.get<WorkshopStateResponse>(`/api/v1/workshops/${active.id}`) 
        : await api.post<WorkshopStateResponse>('/api/v1/workshops', { project_id: projectId, quality_level: qualityLevel });
      
      setWorkshopState(state);
      setQualityLevel(state.session.quality_level);
      if (state.latest_artifact) {
        setRenderArtifact(state.latest_artifact);
        setDocumentContent(state.latest_artifact.report_markdown);
      }
      
      const turns = state.session.turns || [];
      const msgs: Message[] = turns.map(t => ({
        id: t.id,
        role: (t.turn_type === 'user' ? 'user' : 'foli'),
        content: t.turn_type === 'user' ? (t.query || '') : (t.response || '')
      }));
      setMessages(msgs.length ? msgs : [{ id: 'w', role: 'foli', content: state.message || '반갑습니다! 탐구를 시작해볼까요?' }]);
    } finally {
      setIsSessionLoading(false);
    }
    
    // Fetch latest diagnosis
    try {
      const diag = await api.get<any>(`/api/v1/diagnosis/project/${projectId}/latest`);
      if (diag && diag.result_payload) {
        setDiagnosisReport(diag.result_payload);
        setShowDiagnosis(true);
      }
    } catch (e) {
      console.log('No diagnosis found or failed to fetch');
    }
  };

  const handleSend = async (overText?: string) => {
    const text = (overText ?? input).trim();
    if (!text || isTyping) return;
    if (!overText) setInput('');

    setMessages(prev => [...prev, { id: crypto.randomUUID(), role: 'user', content: text }]);
    setIsTyping(true);
    
    const foliId = crypto.randomUUID();
    setMessages(prev => [...prev, { id: foliId, role: 'foli', content: '' }]);
    
    let accumulated = '';
    try {
      const raw = await streamFoliReply(isProjectBacked ? projectId : undefined, text, delta => {
        accumulated += delta;
        setMessages(ps => ps.map(m => m.id === foliId ? { ...m, content: accumulated } : m));
      });

      // Simple tag parsing
      const tagMatch = raw.match(/\[CONTENT\]([\s\S]*?)\[\/CONTENT\]/);
      if (tagMatch) {
         setMessages(ps => ps.map(m => m.id === foliId ? { 
           ...m, 
           content: raw.replace(/\[CONTENT\][\s\S]*?\[\/CONTENT\]/, '').trim(),
           suggestedContent: tagMatch[1].trim()
         } : m));
      } else {
        setMessages(ps => ps.map(m => m.id === foliId ? {
          ...m,
          content: raw || accumulated || 'Foli could not prepare a reply.',
        } : m));
      }

      if (isProjectBacked && workshopState) {
        const updated = await api.post<WorkshopStateResponse>(`/api/v1/workshops/${workshopState.session.id}/messages`, { message: text });
        setWorkshopState(updated);
        if (updated.message) {
           setMessages(p => [...p, { id: crypto.randomUUID(), role: 'foli', content: updated.message! }]);
        }
      }
    } catch (e) {
      console.error('AI Reply Stream Error:', e);
      if (e instanceof Error && e.message === 'RATE_LIMIT') {
        toast.error('메시지 전송 한도를 초과했습니다. 잠시 후 시도해 주세요.');
      } else if (e instanceof Error && e.message === 'AUTH_REQUIRED') {
        toast.error('작업실 대화를 불러오려면 로그인이 필요합니다.');
      } else if (e instanceof Error && e.message === 'NETWORK_FAIL') {
        toast.error('답변 생성 실패 (백엔드 연결 확인)');
      } else if (e instanceof Error) {
        toast.error(e.message === 'STREAM_FAIL' ? '답변 생성 실패 (네트워크 오류)' : e.message);
      } else {
        toast.error('답변 생성 실패');
      }
    } finally {
      setIsTyping(false);
    }
  };

  const qMeta = QUALITY_META_MAP[qualityLevel] || QUALITY_META_MAP.mid;
  
  const applySuggestedContent = (content: string) => {
    if (documentContent.includes(content)) {
      setPendingSuggestion(null);
      return;
    }
    const marker = `${AI_SUGGESTION_HEADER}\n${AI_SUGGESTION_NOTE}`;
    const newDoc = `${documentContent}\n\n${marker}\n\n${content}`;
    
    setDocHistory(prev => {
      const hist = prev.slice(0, historyIndex + 1);
      hist.push(documentContent);
      return hist.slice(-20);
    });
    setHistoryIndex(prev => Math.min(prev + 1, 19));
    
    setDocumentContent(newDoc);
    setPendingSuggestion(null);
    toast.success('제안된 내용이 문서 끝에 추가되었습니다.');
  };

  const undoLastChange = () => {
    if (historyIndex >= 0 && docHistory[historyIndex] !== undefined) {
      setDocumentContent(docHistory[historyIndex]);
      setHistoryIndex(prev => prev - 1);
      toast.success('문서 수정을 취소했습니다.');
    }
  };

  const handleUpdateVisualStatus = async (visualId: string, status: string) => {
    if (!workshopState?.session.id || !workshopState?.latest_artifact?.id) return;
    try {
      let updated: WorkshopStateResponse;
      if (status === 'replaced') {
        updated = await api.post<WorkshopStateResponse>(
          `/api/v1/workshops/${workshopState.session.id}/artifacts/${workshopState.latest_artifact.id}/visuals/${visualId}/replace`
        );
      } else {
        updated = await api.patch<WorkshopStateResponse>(
          `/api/v1/workshops/${workshopState.session.id}/artifacts/${workshopState.latest_artifact.id}/visuals/${visualId}`,
          { approval_status: status }
        );
      }
      setWorkshopState(updated);
      const toastMsg = {
        approved: '시각 자료를 보고서에 포함했습니다.',
        rejected: '시각 자료를 제외했습니다.',
        replaced: '새로운 시각적 대안을 생성했습니다.',
      }[status] || '상태를 업데이트했습니다.';
      toast.success(toastMsg);
    } catch (e) {
      console.error('Failed to update visual status', e);
      toast.error('요청 처리 실패');
    }
  };

  return (
    <div className="mx-auto flex h-[calc(100vh-8rem)] max-w-7xl flex-col gap-6 p-4 lg:flex-row lg:px-8">
      {/* Left Panel: Chat */}
      <div className="flex h-[50vh] w-full flex-col rounded-3xl border bg-white shadow-sm lg:h-full lg:w-[40%] overflow-hidden relative">
        <div className="p-4 border-b flex items-center justify-between bg-white z-10">
          <div className="flex items-center gap-2">
            <Bot size={20} className="text-blue-500" />
            <h2 className="text-sm font-black text-slate-800">Foli 작업실</h2>
          </div>
          <div className={`px-2.5 py-1 rounded-lg text-[10px] font-black ${qMeta.bg} ${qMeta.text}`}>
            {qualityLevel.toUpperCase()} LEVEL
          </div>
        </div>

        {diagnosisReport && (
          <div className="bg-slate-50 border-b overflow-hidden transition-all">
             <button 
               onClick={() => setShowDiagnosis(!showDiagnosis)}
               className="w-full px-4 py-2 flex items-center justify-between text-[11px] font-black text-slate-400 hover:bg-slate-100 transition-colors"
             >
                <div className="flex items-center gap-2">
                  <Sparkles size={12} className="text-blue-500" />
                  <span>AI 진단 리포트 요약</span>
                </div>
                {showDiagnosis ? <ChevronUp size={14} /> : <ChevronDown size={14} />}
             </button>
             <AnimatePresence>
               {showDiagnosis && (
                 <motion.div 
                   initial={{ height: 0, opacity: 0 }}
                   animate={{ height: 'auto', opacity: 1 }}
                   exit={{ height: 0, opacity: 0 }}
                   className="px-4 pb-4"
                 >
                    <div className="p-3 bg-white rounded-xl border border-blue-100 shadow-sm">
                       <p className="text-xs font-black text-slate-800 leading-tight mb-2">{diagnosisReport.headline}</p>
                       <div className="flex gap-2 mb-2">
                          <span className={`text-[9px] font-black px-1.5 py-0.5 rounded ${diagnosisReport.risk_level === 'safe' ? 'bg-emerald-50 text-emerald-600' : 'bg-amber-50 text-amber-600'}`}>
                            {diagnosisReport.risk_level.toUpperCase()}
                          </span>
                          <span className="text-[9px] font-black text-slate-400">GAPS: {diagnosisReport.gaps?.length ?? 0}</span>
                       </div>
                       <p className="text-[10px] font-bold text-slate-500 leading-relaxed italic border-l-2 border-blue-500 pl-2">
                          "{diagnosisReport.recommended_focus}"
                       </p>
                    </div>
                 </motion.div>
               )}
             </AnimatePresence>
          </div>
        )}

        <div
          className="flex-1 overflow-y-auto p-4 space-y-4 bg-slate-50/20"
          tabIndex={0}
          aria-label="Workshop conversation"
        >
          {messages.map(m => (
            <ChatBubble
              key={m.id}
              message={m}
              onSuggestClick={(content: string) => setPendingSuggestion(content)}
            />
          ))}
          {isTyping && (
             <div className="flex gap-1.5 p-2">
               <div className="w-1.5 h-1.5 bg-blue-400 rounded-full animate-bounce" />
               <div className="w-1.5 h-1.5 bg-blue-400 rounded-full animate-bounce [animation-delay:0.2s]" />
               <div className="w-1.5 h-1.5 bg-blue-400 rounded-full animate-bounce [animation-delay:0.4s]" />
             </div>
          )}
          <div ref={messagesEndRef} />
        </div>

        <div className="p-4 border-t bg-white space-y-3">
          {isProjectBacked && advancedMode && (
            <ReferenceSearchPanel 
               isAdvancedMode={advancedMode} 
               onPinReference={async (text, source) => {
                  if (!workshopState) return;
                  const res = await api.post<WorkshopStateResponse>(`/api/v1/workshops/${workshopState.session.id}/references/pin`, { text_content: text, source_type: source });
                  setWorkshopState(res);
                  toast.success('자료를 고정했습니다.');
               }} 
            />
          )}
          {!isProjectBacked && advancedMode && (
            <div className="rounded-2xl border border-amber-200 bg-amber-50 px-4 py-3 text-xs font-semibold leading-relaxed text-amber-800">
              Evidence search and pinned references unlock after you start a project-backed workshop.
            </div>
          )}

          <div className="flex items-center gap-2">
            <button
              type="button"
              aria-label="Export draft"
              onClick={() => toast.success('준비 중')}
              className="p-2.5 bg-slate-50 text-slate-400 rounded-xl hover:bg-slate-100"
            >
               <Download size={18} className="rotate-180" />
            </button>
            <input 
              value={input} 
              onChange={e => setInput(e.target.value)} 
              onKeyDown={e => e.key === 'Enter' && handleSend()} 
              placeholder="메시지를 입력하세요..." 
              className="flex-1 px-4 py-2.5 bg-slate-50 border border-transparent rounded-2xl text-sm focus:outline-none focus:border-blue-400 transition-colors"
            />
            <button 
              type="button"
              aria-label="Send message to Foli"
              onClick={() => handleSend()} 
              disabled={!input.trim() || isTyping}
              className="p-2.5 bg-blue-500 text-white rounded-xl disabled:bg-slate-200 transition-colors"
            >
              <Send size={18} />
            </button>
          </div>

          <div className="flex items-center justify-between px-2 pt-1">
             <span className="text-[10px] font-black text-slate-400 uppercase tracking-widest">Advanced Features</span>
             <button
                type="button"
                aria-label={advancedMode ? 'Disable advanced features' : 'Enable advanced features'}
                data-testid="workshop-advanced-toggle"
                onClick={() => setAdvancedMode(!advancedMode)}
                className={`transition-colors ${advancedMode ? 'text-blue-500' : 'text-slate-200'}`}
             >
                {advancedMode ? <ToggleRight size={24} /> : <ToggleLeft size={24} />}
             </button>
          </div>
        </div>
      </div>

      {/* Right Panel: Document */}
      <div className="flex-1 flex flex-col rounded-3xl border border-slate-700 bg-slate-800 overflow-hidden shadow-2xl min-h-[50vh] relative">
        <SuggestionReviewModal 
          isOpen={!!pendingSuggestion} 
          content={pendingSuggestion}
          onApply={() => pendingSuggestion && applySuggestedContent(pendingSuggestion)}
          onCancel={() => setPendingSuggestion(null)}
        />
        <div className="h-14 px-6 flex items-center justify-between bg-slate-900 border-b border-slate-700">
          <div className="flex items-center gap-2">
             <div className="w-2 h-2 rounded-full bg-emerald-500 animate-pulse" />
             <span className="text-xs text-slate-400 font-bold truncate max-w-[200px]">{fileName}</span>
          </div>
          <div className="flex gap-2">
            <button 
                type="button"
                data-testid="workshop-undo"
                disabled={historyIndex < 0}
                onClick={undoLastChange}
                className="inline-flex items-center gap-2 px-3 py-1.5 bg-slate-800 text-slate-300 text-[11px] font-black rounded-xl hover:bg-slate-700 disabled:opacity-50 transition-colors"
            >
               <RotateCcw size={14} /> 실행 취소
            </button>
            <button 
              onClick={async () => {
                if (!workshopState || isRendering) return;
                setIsRendering(true);
                try {
                  await api.post(`/api/v1/workshops/${workshopState.session.id}/render`);
                  toast.success('초안 생성을 예약했습니다.');
                } finally {
                  setIsRendering(false);
                }
              }}
              className="inline-flex items-center gap-2 px-3 py-1.5 bg-blue-600 text-white text-[11px] font-black rounded-xl hover:bg-blue-700 transition-colors"
            >
              <WandSparkles size={14} /> {isRendering ? '생성 중...' : '전문화 초안'}
            </button>
            <button 
                type="button"
                data-testid="workshop-save-draft"
                onClick={() => {
                  saveArchiveItem({
                    id: crypto.randomUUID(),
                    projectId: projectId ?? null,
                    title: fileName.replace('.hwpx', ''),
                    subject: initialMajor,
                    createdAt: new Date().toISOString(),
                    contentMarkdown: documentContent
                  });
                  toast.success('아카이브에 저장되었습니다.');
                  confetti({ particleCount: 100, spread: 70, origin: { y: 0.6 } });
                }}
                className="px-4 py-1.5 bg-white text-slate-900 text-[11px] font-black rounded-xl hover:bg-slate-50 transition-colors"
            >
               저장
            </button>
          </div>
        </div>

        <div
          className="flex-1 overflow-y-auto p-4 sm:p-10 bg-slate-800/40"
          tabIndex={0}
          aria-label="Draft preview document"
          data-testid="workshop-draft-preview"
        >
           <div className="mx-auto mb-4 max-w-[210mm] rounded-2xl border border-blue-200 bg-blue-50 px-4 py-3 text-xs font-semibold leading-relaxed text-blue-900">
             Student-authored draft stays primary. AI text is added only after an explicit click and remains marked as AI-generated draft material.
           </div>
           <div className="mx-auto max-w-[210mm] bg-white p-12 shadow-2xl min-h-[297mm] rounded-sm">
             <div className="prose prose-sm prose-slate max-w-none prose-headings:font-black">
               {documentContent ? (
                 <MemoizedMarkdown content={documentContent} />
               ) : (
                 <div className="flex flex-col items-center justify-center py-20 text-center">
                   <div className="mb-6 flex h-20 w-20 items-center justify-center rounded-[2rem] bg-slate-50 text-slate-300 shadow-inner mx-auto">
                     <FileText size={40} />
                   </div>
                   <h3 className="mb-2 text-xl font-black text-slate-400">보고서 초안이 비어 있습니다.</h3>
                   <p className="max-w-xs text-sm font-bold text-slate-300 leading-relaxed mx-auto">
                     왼쪽 Foli 작업실에서 대화를 통해 탐구 동기와 내용을 구체화하고 보고서에 반영해 보세요.
                   </p>
                 </div>
               )}
             </div>
           </div>
           
           <EvidenceDrawer evidenceMap={renderArtifact?.evidence_map ?? null} />

           <div className="mt-8">
               <AdvancedPreview 
                 workshopId={workshopState?.session.id || ''}
                 artifactId={workshopState?.latest_artifact?.id || ''}
                 isAdvancedMode={advancedMode} 
                 visualSpecs={renderArtifact?.visual_specs ?? []} 
                 mathExpressions={renderArtifact?.math_expressions ?? []} 
                 onUpdateVisualStatus={handleUpdateVisualStatus}
               />
           </div>
        </div>
      </div>
    </div>
  );
}
