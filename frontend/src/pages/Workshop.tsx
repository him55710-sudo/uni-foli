const MemoizedMarkdown = memo(function MemoizedMarkdown({
  content,
  role = 'foli',
}: {
  content: string;
  role?: MessageRole | 'document';
}) {
  const isUserMessage = role === 'user';

  return (
    <ReactMarkdown
      remarkPlugins={[remarkMath]}
      rehypePlugins={[rehypeKatex]}
      components={{
        h3: ({ children, ...props }) => {
          const text = React.Children.toArray(children).join('');
          const isAISuggestion = !isUserMessage && (text.includes('[AI 제안 초안]') || text.includes('[AI Draft Suggestion]'));

          if (isAISuggestion) {
            return (
              <h3
                {...props}
                className="mb-4 mt-8 flex items-center gap-2 rounded-xl border border-blue-200 bg-blue-50 px-3 py-2 text-sm font-bold text-blue-800"
              >
                <span className="inline-flex h-5 w-5 items-center justify-center rounded-lg bg-blue-600 text-[10px] font-black text-white">AI</span>
                {children}
              </h3>
            );
          }

          return <h3 {...props} className={isUserMessage ? 'mb-3 mt-5 text-base font-bold text-slate-100' : 'mb-3 mt-6 text-lg font-bold text-slate-900'} />;
        },
        p: ({ children, ...props }) => {
          const text = React.Children.toArray(children).join('');
          const isAINote =
            !isUserMessage && (text.includes('Foli가 생성한 제안 문장입니다') || text.includes('This text was generated by Foli'));

          if (isAINote) {
            return (
              <p
                {...props}
                className="mb-5 rounded-lg border-l-4 border-blue-300 bg-blue-50 px-3 py-2 text-sm font-medium italic text-slate-600"
              />
            );
          }

          return <p {...props} className={isUserMessage ? 'whitespace-pre-wrap text-sm leading-7 text-slate-100' : 'text-sm leading-7 text-slate-700'} />;
        },
      }}
    >
      {content}
    </ReactMarkdown>
  );
});

